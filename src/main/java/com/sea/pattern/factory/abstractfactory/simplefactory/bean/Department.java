package com.sea.pattern.factory.abstractfactory.simplefactory.bean;

/**
 * 
 * 部门信息
 *
 * @author huachengwu (mailto:huachengwu@tansun.com.cn)
 */
public class Department {
	private String id;
	private String name;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
